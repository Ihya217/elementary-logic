
#if else biasa
umur = 18 #type string

if umur >= 18
  puts "Anda dewasa"
else
  puts "Anda masih anak-anak"
end


#elseif
nilai = 80

if nilai >= 90
  puts "A"
elsif nilai >= 80
  puts "B"
elsif nilai >= 70
  puts "C"
else
  puts "Tidak Lulus"
end


#Struktur if else sederhana tanpa {} dan () kelihatan simple dan dapat mudah dimengerti, menggunakan end di akhir logika
