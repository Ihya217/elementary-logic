counter = 0 #int

while counter < 5               #While digunakan untuk perulangan sederhana
  puts "Iterasi ke-#{counter}"
  counter += 1
end

for i in 1..5                   #For digunakan untuk menggunakan variable index atau yg lainnya
  puts "Iterasi ke-#{i}"
end

